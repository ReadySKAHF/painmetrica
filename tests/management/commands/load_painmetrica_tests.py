# ─── Команда ───────────────────────────────────────────────────────────────────

class Command(BaseCommand):
    help = 'Загружает стандартные тесты VAS + DN4 + CSI + HADS в базу данных'

    def add_arguments(self, parser):
        parser.add_argument(
            '--force',
            action='store_true',
            help='Удалить существующий тест и создать заново',
        )

    def handle(self, *args, **options):
        if options['force']:
            deleted, _ = Test.objects.filter(title=TEST_TITLE).delete()
            if deleted:
                self.stdout.write(self.style.WARNING(f'Удалён существующий тест и {deleted} связанных объектов.'))

        test, created = Test.objects.get_or_create(
            title=TEST_TITLE,
            defaults={'description': 'Батарея из четырёх тестов для оценки болевого синдрома: '
                                     'VAS, DN4, CSI, HADS.'},
        )

        if not created:
            self.stdout.write(self.style.WARNING(
                f'Тест «{TEST_TITLE}» уже существует. Используйте --force для пересоздания.'
            ))
            return

        # Создаём этапы
        stage_objects = {}
        for order, sidebar_step, name, description, page_title, annotation in STAGES:
            stage = Stage.objects.create(
                test=test,
                order=order,
                sidebar_step=sidebar_step,
                name=name,
                description=description,
                page_title=page_title,
                annotation=annotation,
            )
            stage_objects[order] = stage
            self.stdout.write(f'  [OK] Этап {order}: {page_title}')

        # Создаём вопросы и варианты ответов
        for stage_order, block_title, question_text, q_type, options_or_scale, order in QUESTIONS:
            stage = stage_objects[stage_order]

            if q_type == 'scale':
                q = Question.objects.create(
                    stage=stage,
                    block_title=block_title,
                    question_text=question_text,
                    question_type='scale',
                    order=order,
                    scale_min=options_or_scale['scale_min'],
                    scale_max=options_or_scale['scale_max'],
                    scale_labels=options_or_scale['scale_labels'],
                )
            else:
                q = Question.objects.create(
                    stage=stage,
                    block_title=block_title,
                    question_text=question_text,
                    question_type=q_type,
                    order=order,
                )
                for opt_order, (opt_text, score) in enumerate(options_or_scale, start=1):
                    QuestionOption.objects.create(
                        question=q,
                        option_text=opt_text,
                        score=score,
                        order=opt_order,
                    )

        self.stdout.write(f'  [OK] Создано вопросов: {Question.objects.filter(stage__test=test).count()}')

        # Создаём диапазоны баллов
        for sidebar_step, min_score, max_score, label, conclusion in SCORE_RANGES:
            ScoreRange.objects.create(
                test=test,
                sidebar_step=sidebar_step,
                min_score=min_score,
                max_score=max_score,
                label=label,
                conclusion=conclusion,
            )

        self.stdout.write(self.style.SUCCESS(
            f'\nТест «{TEST_TITLE}» успешно создан! ID={test.pk}'
        ))
        self.stdout.write(
            f'Этапов: {test.stages.count()}, '
            f'Вопросов: {Question.objects.filter(stage__test=test).count()}, '
            f'Диапазонов баллов: {test.score_ranges.count()}'
        )
